#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		i;
	size_t	len;
	char	*dst;

	i = 0;
	len = ft_strlen(src);
	dst = (char *)malloc(sizeof(*src) * (len + 1));
	if (dst == 0)
		return (NULL);
	while (src[i] != '\0')
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}

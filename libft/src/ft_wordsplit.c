#include "libft.h"

int		ft_wordsplit(char const *s, char c)
{
	int		i;
	int		wd;

	i = 0;
	wd = 0;
	while (s[i] != '\0')
	{
		if (s[i] != c)
			wd++;
		i++;
	}
	return (wd);
}
